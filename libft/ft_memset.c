#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*ptr;

	ptr = (char *)s;
	i = -1;
	while (++i < n)
		ptr[i] = (unsigned char)c;
	return (s);
}
